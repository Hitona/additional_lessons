# Задайте массив заполненный случайными трёхзначными числами. Напишите программу, которая покажет количество чётных чисел в массиве.

import random

# Создаём массив из 10 случайных трёхзначных чисел
numbers = [random.randint(100, 999) for _ in range(10)]

# Подсчитываем количество чётных чисел в массиве
even_count = sum(1 for num in numbers if num % 2 == 0)

print(f"Массив: {numbers}")
print(f"Количество чётных чисел: {even_count}")
